# Число Фбаначчи с помощью функции
# Долго выщитывает!
def fibonacci(m):
    if m <= 2:
        return 1 # по тому что первое и второе число Фибоначчи это 1 и 1
    return fibonacci(m - 1) + fibonacci(m - 2)

print('\n\nЧисло Фбаначчи с помощью функции:', fibonacci(m))



                